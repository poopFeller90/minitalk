#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*p_s;

	p_s = NULL;
	while (*s)
	{
		if (*s == (char)c)
			p_s = (char *)s;
		s++;
	}
	if ((char)c == '\0')
		return ((char *)s);
	return (p_s);
}
